package ru.angelui.ui;

public class BasicGuiElement {

    protected String id;
    protected int x, y, sizeX, sizeY;

    public String getId() {
        return id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getSizeX() {
        return sizeX;
    }

    public int getSizeY() {
        return sizeY;
    }
}
